    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String customerid = jTextField_cid1.getText();
        String payment = jTextField_price.getText();
       
        

        if(customerid.equals("")){

            JOptionPane.showMessageDialog(null, "Enter Your User ID");

        }

        else if(payment.equals("")){

            JOptionPane.showMessageDialog(null, "Enter The Amount");
        }

       
  
        else{

            PreparedStatement ps;

            String query = "INSERT INTO `payment_table`(`customerid`, `payment`) VALUES(?,?)";

            try {
                ps = MyConnection.getConnection().prepareStatement(query);
                
                
                ps.setString(1, customerid);
                ps.setString(2, payment);
                

                if(ps.executeUpdate() > 0){

                    JOptionPane.showMessageDialog(null, "Payment Details Added");
                }

            } catch (SQLException ex) {
                Logger.getLogger(registerf.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

